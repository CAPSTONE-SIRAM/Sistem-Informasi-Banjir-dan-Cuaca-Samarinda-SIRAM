    private void setupTableClick() {
    tblLaporan.getSelectionModel().addListSelectionListener(e -> {
        if (!e.getValueIsAdjusting()) {
            int row = tblLaporan.getSelectedRow();
            if (row >= 0) {
                txtID.setText(tblLaporan.getValueAt(row, 0).toString());
            }
        }
    });
}
    private String formatStatus(String status) {
    if (status == null) return "";
    return status.toLowerCase()
                 .replace("_", " ");
    }
    private void loadLaporan() {
    try {
        Connection conn = Koneksi.Koneksi.getConnection();
        String sql = "SELECT id_laporan, tanggal_laporan, tinggi_air, lokasi, "
                   + "d.kecamatan, u.nama_lengkap, status_banjir,foto_bukti "
                   + "FROM laporan l "
                   + "JOIN users u ON l.id_user = u.id_user "
                   + "JOIN daerah d ON l.id_daerah = d.id_daerah "
                   + "ORDER BY l.id_laporan DESC";

        PreparedStatement pst = conn.prepareStatement(sql);

        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = new DefaultTableModel(
            new Object[]{"ID", "Tanggal", "Nama Pelapor", "Kecamatan", "Tinggi Air(cm)", "Lokasi", "Status","Foto Bukti"}, 0
        ){
    @Override
    public boolean isCellEditable(int row, int column) {
        return column == 7 ;
    }
};

        while (rs.next()) {
            byte[] fotoBytes = rs.getBytes("foto_bukti");
            Object fotoCell = (fotoBytes != null && fotoBytes.length > 0)
                    ? fotoBytes
                    : "Tidak Ada";
            model.addRow(new Object[]{
                rs.getInt("id_laporan"),
                rs.getString("tanggal_laporan"),
                rs.getString("nama_lengkap"),
                rs.getString("kecamatan"),
                rs.getDouble("tinggi_air"),
                rs.getString("lokasi"),
                formatStatus(rs.getString("status_banjir")),
                fotoCell
            });
        }

        tblLaporan.setModel(model);
        tblLaporan.setRowHeight(35);
        tblLaporan.getColumnModel().getColumn(7).setCellRenderer(new ButtonRenderer());
        tblLaporan.getColumnModel().getColumn(7).setCellEditor(new ButtonEditor(new JCheckBox(), 7));
        tblLaporan.getTableHeader().setReorderingAllowed(false);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        e.printStackTrace();
    }
}

