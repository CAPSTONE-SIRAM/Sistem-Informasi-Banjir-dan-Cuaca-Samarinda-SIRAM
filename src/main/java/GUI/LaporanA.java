    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
         String idText = txtID.getText();
    if (idText.isBlank()) {
        JOptionPane.showMessageDialog(this, "Pilih laporan terlebih dahulu!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int idLaporan = Integer.parseInt(idText);

    try (Session session = Util.HibernateUtil.getSessionFactory().openSession()) {
        Transaction tx = session.beginTransaction();
        Model.Laporan laporan = session.get(Model.Laporan.class, idLaporan);
        if (laporan != null) {
            // Toggle status banjir
            if (laporan.getStatus() == Laporan.StatusBanjir.MASIH_BANJIR) {
                laporan.setStatus(Laporan.StatusBanjir.SUDAH_SURUT);
            } else {
                laporan.setStatus(Laporan.StatusBanjir.MASIH_BANJIR);
            }
            session.merge(laporan);
            tx.commit();
            JOptionPane.showMessageDialog(this, "Status laporan berhasil diubah!");
            loadLaporan(); // refresh tabel
        } else {
            JOptionPane.showMessageDialog(this, "Laporan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal mengubah status: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
         String idText = txtID.getText();
    if (idText.isBlank()) {
        JOptionPane.showMessageDialog(this, "Pilih laporan terlebih dahulu!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    int idLaporan = Integer.parseInt(idText);

    int confirm = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus laporan ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (confirm != JOptionPane.YES_OPTION) return;

    try (Session session = Util.HibernateUtil.getSessionFactory().openSession()) {
        Transaction tx = session.beginTransaction();
        Model.Laporan laporan = session.get(Model.Laporan.class, idLaporan);
        if (laporan != null) {
            session.remove(laporan);
            tx.commit();
            JOptionPane.showMessageDialog(this, "Laporan berhasil dihapus!");
            loadLaporan(); // refresh tabel
        } else {
            JOptionPane.showMessageDialog(this, "Laporan tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (Exception e) {

    Throwable cause = e.getCause();
    while (cause != null) {
        if (cause instanceof java.sql.SQLIntegrityConstraintViolationException) {
            JOptionPane.showMessageDialog(this,
                "Laporan ini memiliki tindak lanjut dan tidak bisa dihapus!",
                "Tidak Bisa Dihapus",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        cause = cause.getCause();
    }

    JOptionPane.showMessageDialog(this,
        "Gagal menghapus laporan: " + e.getMessage(),
        "Error",
        JOptionPane.ERROR_MESSAGE);
    
    e.printStackTrace();
}
    }//GEN-LAST:event_btnHapusActionPerformed

