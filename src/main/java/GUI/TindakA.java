    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
    try {
    // === VALIDASI ID Laporan ===
    String idText = txtIDLaporan.getText().trim();
    if (idText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "ID Laporan wajib diisi!");
        return;
    }

    int idLaporan;
    try {
        idLaporan = Integer.parseInt(idText);
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "ID Laporan harus berupa angka!");
        return;
    }

    Connection conn = Koneksi.Koneksi.getConnection();
    PreparedStatement cek = conn.prepareStatement("SELECT id_laporan FROM laporan WHERE id_laporan = ?");
    cek.setInt(1, idLaporan);
    ResultSet rs = cek.executeQuery();
    if (!rs.next()) {
        JOptionPane.showMessageDialog(this, "ID Laporan tidak ditemukan!");
        conn.close();
        return;
    }

    String bentuk = txtBentuk.getText().trim();
    if (bentuk.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Bentuk penanganan wajib diisi!");
        conn.close();
        return;
    }
        // Minimal 5 huruf (mengabaikan angka dan spasi)
    if (bentuk.replaceAll("[^a-zA-Z]", "").length() < 5) {
        JOptionPane.showMessageDialog(this, 
            "Bentuk penanganan harus mengandung minimal 5 huruf!",
            "Input Tidak Valid", JOptionPane.WARNING_MESSAGE);
        conn.close();
        return;
    }

    // Boleh huruf, angka, spasi, dan tanda baca ringan
    if (!bentuk.matches("[a-zA-Z0-9 .,!?()'-]+")) {
        JOptionPane.showMessageDialog(this,
            "Bentuk penanganan hanya boleh berisi huruf, angka, spasi, dan tanda baca dasar!",
            "Input Tidak Valid", JOptionPane.WARNING_MESSAGE);
        conn.close();
        return;
    }

    if (bentuk.length() > 200) {
        JOptionPane.showMessageDialog(this, "Bentuk penanganan terlalu panjang (maksimal 200 karakter)!");
        conn.close();
        return;
    }

    String sql = "INSERT INTO tindak_lanjut (id_laporan, tanggal_tindak, status, bentuk_penanganan) VALUES (?, ?, ?, ?)";
    PreparedStatement pst = conn.prepareStatement(sql);

    pst.setInt(1, idLaporan);
    pst.setDate(2, java.sql.Date.valueOf(java.time.LocalDate.now()));
    pst.setString(3, "DIPROSES");
    pst.setString(4, bentuk);

    pst.executeUpdate();
    conn.close();

    JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan");
    loadTindakLanjut();

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Gagal menambah data: " + e.getMessage());
}
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        String idText = txtIDTindak.getText().trim();
    if (idText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Pilih data yang akan dihapus!");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this,
            "Yakin ingin menghapus data ini?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION);

    if (confirm != JOptionPane.YES_OPTION) return;

    try {
        int idTindak = Integer.parseInt(idText);
        Connection conn = Koneksi.Koneksi.getConnection();

        // Cek apakah data ada dulu
        PreparedStatement cek = conn.prepareStatement("SELECT id_tindak FROM tindak_lanjut WHERE id_tindak=?");
        cek.setInt(1, idTindak);
        ResultSet rs = cek.executeQuery();

        if (!rs.next()) {
            JOptionPane.showMessageDialog(this, "ID Tindak tidak ditemukan!");
            conn.close();
            return;
        }

        PreparedStatement pst = conn.prepareStatement("DELETE FROM tindak_lanjut WHERE id_tindak=?");
        pst.setInt(1, idTindak);
        pst.executeUpdate();

        conn.close();
        JOptionPane.showMessageDialog(this, "Data berhasil dihapus ✅");
        loadTindakLanjut();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menghapus: " + e.getMessage());
    }
    }//GEN-LAST:event_btnHapusActionPerformed

