    private void btnPerbaruiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPerbaruiActionPerformed
        String idText = txtIDTindak.getText().trim();
    if (idText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Pilih data terlebih dahulu!");
        return;
    }

    try {
        int idTindak = Integer.parseInt(idText);

        Connection conn = Koneksi.Koneksi.getConnection();

        // Cek apakah id_tindak ada di DB
        PreparedStatement cek = conn.prepareStatement("SELECT id_tindak FROM tindak_lanjut WHERE id_tindak = ?");
        cek.setInt(1, idTindak);
        ResultSet rs = cek.executeQuery();

        if (!rs.next()) {
            JOptionPane.showMessageDialog(this, "ID Tindak Lanjut tidak ditemukan!");
            conn.close();
            return;
        }

        String bentuk = txtBentuk.getText().trim();
        if (bentuk.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Bentuk penanganan wajib diisi!");
            conn.close();
            return;
        }

        String sql = "UPDATE tindak_lanjut SET id_laporan=?, tanggal_tindak=?, bentuk_penanganan=? WHERE id_tindak=?";
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setInt(1, Integer.parseInt(txtIDLaporan.getText()));
        pst.setDate(2, java.sql.Date.valueOf(java.time.LocalDate.now()));
        pst.setString(3, bentuk);
        pst.setInt(4, idTindak);
        pst.executeUpdate();

        conn.close();
        JOptionPane.showMessageDialog(this, "Data berhasil diperbarui ✅");
        loadTindakLanjut();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memperbarui: " + e.getMessage());
    }
    }//GEN-LAST:event_btnPerbaruiActionPerformed

