    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setDialogTitle("Pilih Foto Bukti");
chooser.addChoosableFileFilter(
    new javax.swing.filechooser.FileNameExtensionFilter("Gambar (*.jpg, *.jpeg, *.png)", "jpg", "jpeg", "png")
);
chooser.setFileFilter(chooser.getChoosableFileFilters()[0]);

        int result = chooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            java.io.File file = chooser.getSelectedFile();

            long maxSize = 16 * 1024 * 1024; 

            // Validasi ukuran file
            if (file.length() > maxSize) {
                JOptionPane.showMessageDialog(this, 
                    "Ukuran file terlalu besar!.", 
                    "File Terlalu Besar", 
                    JOptionPane.WARNING_MESSAGE);
                return;
            }
            try {
        java.awt.image.BufferedImage img = javax.imageio.ImageIO.read(file);
        if (img == null) {
            JOptionPane.showMessageDialog(this,
                "File bukan gambar yang valid!",
                "Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                "File tidak dapat dibaca sebagai gambar!",
                "Error",
                JOptionPane.ERROR_MESSAGE);
            return;
        }

            try (java.io.FileInputStream fis = new java.io.FileInputStream(file)) {
                fotoBytes = fis.readAllBytes();
                txtAFile.setText(file.getAbsolutePath());
                JOptionPane.showMessageDialog(this, "Foto berhasil dipilih: " + file.getName());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Gagal mengungah foto", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnUploadActionPerformed

    private void btnKirimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKirimActionPerformed
        if (txtTinggi.getText().isBlank() || txtLokasi.getText().isBlank()){
        JOptionPane.showMessageDialog(this, "Tinggi air dan lokasi harus diisi!", "Error", JOptionPane.ERROR_MESSAGE);
        return;
        }

        int idDaerah = getSelectedDaerahId();
        if (idDaerah == -1) {
            JOptionPane.showMessageDialog(this, "Pilih kecamatan!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            double tinggiAir = Double.parseDouble(txtTinggi.getText());
            String lokasi = txtLokasi.getText();
            Daerah daerah = getDaerahById(idDaerah);
            Users user = getUserById(userId);

            if (tinggiAir > 150) {
                JOptionPane.showMessageDialog(this, "Tinggi air tidak masuk akal");
            return;
            }
            if (lokasi.isBlank()) {
            JOptionPane.showMessageDialog(this, "Lokasi wajib diisi!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
            }
            if (lokasi.length() > 500) {
            JOptionPane.showMessageDialog(this, "Lokasi terlalu panjang");
            return;
            }
            if (!lokasi.matches(".*[a-zA-Z].*")) {
                JOptionPane.showMessageDialog(this, "Lokasi harus mengandung huruf!", "Input Tidak Valid", JOptionPane.WARNING_MESSAGE);
            return;
            }
            
            if (!lokasi.matches("[a-zA-Z0-9 .,'/-]+")) {
                JOptionPane.showMessageDialog(this, "Lokasi hanya boleh berisi huruf, angka, spasi, titik(.), koma(,), strip(-), apostrof(') atau garis miring(/)", "Input Tidak Valid", JOptionPane.WARNING_MESSAGE);
             return;
            }
            if (daerah == null) {
                JOptionPane.showMessageDialog(this, "Daerah tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (user == null) {
                JOptionPane.showMessageDialog(this, "User tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Laporan laporan = new Laporan();
            laporan.setTanggalLaporan(LocalDate.now());
            laporan.setTinggiAir((float) tinggiAir);
            laporan.setLokasi(lokasi);
            laporan.setFotoBukti(fotoBytes);
            laporan.setStatus(Laporan.StatusBanjir.MASIH_BANJIR);
            laporan.setDaerah(daerah);
            laporan.setUser(user);

            LaporanDAO dao = new LaporanDAOImpl();
            dao.tambah(laporan);

            JOptionPane.showMessageDialog(this, "Laporan berhasil dikirim!");
            resetForm();

            LaporanU laporanU = new LaporanU(userId);
            laporanU.setVisible(true);
            this.dispose();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Tinggi air harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        }

        private Daerah getDaerahById(int idDaerah) {
            try (Session session = HibernateUtil.getSessionFactory().openSession()) {
                return session.get(Daerah.class, idDaerah);
            }
        }

        private Users getUserById(int idUser) {
            try (Session session = HibernateUtil.getSessionFactory().openSession()) {
                return session.get(Users.class, idUser);
            }
        }

        private void resetForm() {
            txtTinggi.setText("");
            txtLokasi.setText("");
            txtAFile.setText("");
            fotoBytes = null;
            if (jComboBox1.getItemCount() > 0) jComboBox1.setSelectedIndex(0);

    }//GEN-LAST:event_btnKirimActionPerformed

