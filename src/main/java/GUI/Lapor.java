    private void loadKecamatan() {
    try {
            java.sql.Connection conn = Koneksi.Koneksi.getConnection();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet rs = stm.executeQuery("SELECT id_daerah, kecamatan FROM daerah");

            jComboBox1.removeAllItems();
            while (rs.next()) {
                jComboBox1.addItem(rs.getInt("id_daerah") + " - " + rs.getString("kecamatan"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load kecamatan: " + e.getMessage());
        }}
    private int getSelectedDaerahId() {
        if (jComboBox1.getSelectedItem() == null) return -1;
        String selected = jComboBox1.getSelectedItem().toString();
        return Integer.parseInt(selected.split(" - ")[0]);
}

